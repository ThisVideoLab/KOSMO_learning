package chap19.EX03;

import java.io.UnsupportedEncodingException;

// EUCKR = 한국 표준 .한글 표현가능 전체 중 표기 가능한 것만 구성되어 있음 .약 11000개 중에서 2200개만 표기 가능하고 8000자는 표기 못함.
//         영문: 1byte
//         한글: 2byte

// MS-949 = Microsoft사에서 개발한 한글 표기 방식. 11000 모두 표기 가능함.
//          영문: 1byte
//          한글: 2byte

//UTF-8: 모든 서버 (Web, FTP, Mail, ...) git, MAC, < 가변길이>
//영문: 1byte
//한글: 3byte

//UTF-16: <고정길이>
//        영문, 한글 모두 2byte로 처리함
//        BOM 2byte: 모든 나라의 식별 코드

// 문자를 해당 인코딩 타입으로 바이트 스트림으로 생성한 경우, 해당 인코딩 타입으로 조합을 해야 깨어지지 않는다.

// 이클립스에서 인코딩 타입 설정 3가지.
//1. 전역 설정:
//2. 프로젝트에서 설정:
//3. 파일에서 생성:


public class EUCKR_VS_MS949 {
	public static void main(String[] args) throws UnsupportedEncodingException {
		
		// EUCKR VS MS-949 
		
		//1. 영문자를 byte로 내보내고 String으로 변환함 : 
		byte[] b1 = "a".getBytes("EUC-KR");  // 문자를 byte로 변환 ==> 네트워크 문자를 byte 스트림으로 변환해서 전송함
		// 예외가 발생함. (UnsupportedEncodingException) < -- 오타가 발생할 경우 인코딩 할 수 없는 예외가 발생이 됨.
		
		byte[] b2 = "a".getBytes("MS949");
		
		System.out.println(b1.length); // 1글자 영어라 1byte가 출력
		System.out.println(b2.length); // 1글자 영어라 1byte가 출력
		System.out.println("===============================");
		
		System.out.println(new String(b1,"EUC-KR")); // byte[] ==> 문자열로 전환함
		System.out.println(new String(b2,"EUC-KR"));
		System.out.println("===============================");
		
		//2. 한글을 byte로 내보내고 String으로 가져오기
		
		byte[] b3 = "가".getBytes("EUC-KR");
		byte[] b4 = "가".getBytes("MS949");
		
		System.out.println(b3.length); // 1글자 한국어라 2byte가 출력
		System.out.println(b4.length); // 1글자 한국어라 2byte가 출력
		
		System.out.println(new String(b3,"EUC-KR")); // byte[] ==> 문자열로 전환함
		System.out.println(new String(b4,"MS949"));
		
		//3. 캐릭터셋 별 문자 표현 능력
		
		byte[] b5 = "쀌".getBytes("EUC-KR"); // 11,000자 중에서 2,000자만 표기하고 8,000자는 표기 못함, 다만 이 8000자는 똲 과 같은 안쓰는 글자들
		byte[] b6 = "쀅".getBytes("MS949"); // 11,000자 모두 처리 가능함
		
		System.out.println(b5.length); // 1
		System.out.println(b6.length); // 2?
		
		System.out.println(new String(b5,"EUC-KR")); // byte[] ==> 문자열로 전환함
		System.out.println(new String(b6,"MS949"));
		
		
	// 문서의끝
	}
}
